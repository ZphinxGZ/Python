# A + B
# + => Operator
# A,B => Opelan 
x = 10
y = 4
print("ผลบวก = ",x + y)
print("ผลลบ = ",x - y)
print("ผลคูณ = ",x * y)
print("ผลหาร = ",x / y)
print("ผลหารปัดเศษ = ",x // y)
print("ผลยกกำลัง = ",x ** y)
print("ผลเศษของกการหาร = ",x % y)